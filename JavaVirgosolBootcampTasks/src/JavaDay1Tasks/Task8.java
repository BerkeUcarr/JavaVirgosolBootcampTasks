package JavaDay1Tasks;

public class Task8 {

    public static void main(String[] args) {

        int sayi1 = 15;
        int sayi2 = 2;

        System.out.println("Toplamı: " + (sayi1 + sayi2));
        System.out.println("Çıkarımı: " + (sayi1 - sayi2));
        System.out.println("Çarpımı: " + (sayi1 * sayi2));
        System.out.println("Bölümü: " + ((double)sayi1 / sayi2));
    }
}
